function down(){
    document.body.style.backgroundColor="red"
    document.body.style.height="90vh"
}


function up(){
    document.body.style.backgroundColor="white"
    document.body.style.height="90vh"
}

function mouseOver(){
    document.body.style.backgroundColor="grey"
    document.body.style.height="90vh"
}


function mouseOut(){
    document.body.style.backgroundColor="white"
    document.body.style.height="90vh"
}